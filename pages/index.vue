<template>
  <div class="min-h-screen bg-gray-50">
    <div class="max-w-7xl mx-auto px-4 py-8">
      <!-- 页面头部 -->
      <header class="text-center mb-8">
        <h1 class="text-4xl font-bold text-indigo-600 mb-2">临时邮箱</h1>
        <p class="text-lg text-gray-600">快速、安全、一次性的临时邮箱服务</p>
      </header>

      <main class="space-y-6">
        <!-- 邮箱地址卡片 -->
        <section class="bg-white rounded-lg shadow-sm p-6 space-y-4">
          <h2 class="text-xl font-semibold text-gray-700">您的临时邮箱地址</h2>
          <div class="flex items-center gap-3">
            <div class="flex-1 bg-gray-50 px-4 py-3 rounded-md border border-gray-200 font-medium text-gray-900 truncate flex items-center min-h-[42px]">
              {{ emailAddress || '等待生成邮箱...' }}
            </div>
            <button
              @click="copyEmail"
              class="min-h-[42px] w-32 flex items-center justify-center gap-1 px-2 py-3 bg-emerald-600 hover:bg-emerald-700 text-white rounded-md transition-colors"
            >
              复制
            </button
            >
          </div>
          <div class="flex gap-2">
            <button
              @click="refreshEmail"
              class="w-32 flex items-center justify-center gap-1 px-2 py-1 bg-indigo-600 hover:bg-indigo-700 text-white rounded-md transition-colors"
              :disabled="isCreatingEmail"
            >
              <span v-if="!isCreatingEmail" class="flex items-center gap-1">
                <svg xmlns="http://www.w3.org/2000/svg" class="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                  <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M4 4v5h.582m15.356 2A8.001 8.001 0 004.582 9m0 0H9m11 11v-5h-.581m0 0a8.003 8.003 0 01-15.357-2m15.357 2H15" />
                </svg>
                创建邮箱
              </span>
              <span v-else class="flex items-center justify-center gap-1">
                <span class="w-4 h-4 border-2 border-white border-t-transparent rounded-full animate-spin"></span>
                创建中...
              </span>
            </button>
            <button
              @click="startAutoCheck"
              class="w-40 flex items-center justify-center gap-1 px-2 py-2 border border-indigo-600 text-indigo-600 hover:bg-gray-50 rounded-md transition-colors"
              :disabled="isChecking"
            >
              <span v-if="!isChecking" class="flex items-center gap-1">
                <svg xmlns="http://www.w3.org/2000/svg" class="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                  <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M3 8l7.89 5.26a2 2 0 002.22 0L21 8M5 19h14a2 2 0 002-2V7a2 2 0 00-2-2H5a2 2 0 00-2 2v10a2 2 0 002 2z" />
                </svg>
                检查新邮件
              </span>
              <span v-else class="flex items-center justify-center gap-1">
                <span class="w-4 h-4 border-2 border-indigo-600 border-t-transparent rounded-full animate-spin"></span>
                正在检查邮件
              </span>
            </button>
          </div>
        </section>

        <!-- 收件箱区域 -->
        <div class="grid grid-cols-1 md:grid-cols-3 gap-6">
          <!-- 邮件列表 -->
          <section class="md:col-span-1 bg-white rounded-lg shadow-sm p-6 h-[500px] flex flex-col">
            <div class="flex justify-between items-center mb-4">
              <h2 class="text-xl font-semibold text-gray-700">收件箱</h2>
              <button 
                @click="clearEmails" 
                class="text-sm px-2 py-1 text-red-600 border border-red-600 rounded hover:bg-red-50 transition-colors"
                :disabled="emails.length === 0"
              >
                清空收件箱
              </button>
            </div>
            <ul v-if="emails.length > 0" class="flex-1 overflow-y-auto space-y-2">
              <li
                v-for="email in emails"
                :key="email.id"
                @click="selectEmail(email)"
                :class="[
                  'p-4 rounded-md cursor-pointer transition-colors',
                  selectedEmail?.id === email.id
                    ? 'bg-indigo-50 border-l-4 border-indigo-600'
                    : 'hover:bg-gray-50'
                ]"
              >
                <div class="flex justify-between mb-1">
                  <span class="font-semibold text-gray-900">{{ email.from }}</span>
                  <span class="text-sm text-gray-500">{{ email.receivedAt }}</span>
                </div>
                <div class="font-medium text-gray-900 truncate">{{ email.subject }}</div>
                <div class="text-sm text-gray-500 truncate">{{ email.preview }}</div>
              </li>
            </ul>
            <div v-else class="flex-1 flex items-center justify-center text-gray-400 text-center">
              <div>
                <div class="text-6xl mb-4">📭</div>
                <div>收件箱中没有邮件</div>
              </div>
            </div>
          </section>

          <!-- 邮件内容 -->
          <section class="md:col-span-2 bg-white rounded-lg shadow-sm p-6 h-[500px] flex flex-col">
            <div v-if="selectedEmail" class="h-full flex flex-col">
              <div class="border-b border-gray-200 pb-4 mb-4">
                <h3 class="text-2xl font-semibold text-gray-900 mb-2">{{ selectedEmail.subject }}</h3>
                <div class="flex justify-between text-sm text-gray-500">
                  <span>发件人: {{ selectedEmail.from }} &lt;</span>
                  <span>{{ selectedEmail.time }}</span>
                </div>
              </div>
              <div class="flex-1 overflow-y-auto prose prose-sm max-w-none" v-if="selectedEmail">
                <!-- 使用安全的方式渲染HTML内容 -->
                <div v-if="isSafeHtml" v-html="sanitizedHtml"></div>
                <pre v-else>{{ selectedEmail.content }}</pre>
              </div>
            </div>
            <div v-else class="h-full flex items-center justify-center text-gray-400 text-center">
              <div>
                <div class="text-6xl mb-4">📨</div>
                <div>选择一封邮件查看详情</div>
              </div>
            </div>
          </section>
        </div>
      </main>
    </div>

    <!-- 通知提示 -->
    <div
      v-if="notification.show"
      :class="[
        'fixed bottom-4 right-4 px-6 py-4 rounded-lg text-white shadow-lg transition-all transform',
        notification.type === 'success' ? 'bg-emerald-600' : 'bg-red-600',
        notification.show ? 'translate-y-0 opacity-100' : 'translate-y-8 opacity-0'
      ]"
    >
      {{ notification.message }}
    </div>
  </div>
</template>

<script setup lang="ts">
interface Email {
  id: number
  sender: string
  senderEmail: string
  time: string
  subject: string
  preview: string
  content: string
  from: string
  receivedAt: number
}

// 状态管理
const emailAddress = ref('')
const emails = ref<Email[]>([])
const selectedEmail = ref<Email | null>(null)
const isChecking = ref(false)
const isCreatingEmail = ref(false)
const autoCheckTimer = ref<number | null>(null)
const autoCheckEndTimer = ref<number | null>(null)
const notification = reactive({
  show: false,
  message: '',
  type: 'success' as 'success' | 'error'
})

// HTML 安全相关
const isSafeHtml = ref(false)
const sanitizedHtml = ref('')

// 监听选中邮件的变化，处理HTML内容
watch(selectedEmail, (email) => {
  if (email) {
    // 检查内容是否为HTML
    const isHtml = email.content.trim().startsWith('<') && email.content.includes('</');
    isSafeHtml.value = isHtml;
    
    if (isHtml) {
      // 简单的HTML净化，移除危险标签和属性
      sanitizedHtml.value = sanitizeHtml(email.content);
    } else {
      sanitizedHtml.value = '';
    }
  }
});

onMounted(() => {
  // 尝试从 localStorage 读取上次生成的邮件地址
  const savedEmail = localStorage.getItem('tempEmailAddress')
  
  if (savedEmail) {
    // 如果本地存在邮件地址，则使用它
    emailAddress.value = savedEmail
    // 尝试获取该邮箱的邮件
    checkNewMails()
  } else {
    // 如果本地没有存储邮件地址，则创建一个新的
    refreshEmail()
  }
})

// 简单的HTML净化函数
function sanitizeHtml(html: string): string {
  // 移除脚本和危险属性
  let sanitized = html
    .replace(/<script\b[^<]*(?:(?!<\/script>)<[^<]*)*<\/script>/gi, '')
    .replace(/<iframe\b[^<]*(?:(?!<\/iframe>)<[^<]*)*<\/iframe>/gi, '')
    .replace(/<object\b[^<]*(?:(?!<\/object>)<[^<]*)*<\/object>/gi, '')
    .replace(/<embed\b[^<]*(?:(?!<\/embed>)<[^<]*)*<\/embed>/gi, '')
    .replace(/javascript:/gi, '')
    .replace(/onerror=/gi, '')
    .replace(/onclick=/gi, '')
    .replace(/onload=/gi, '')
    .replace(/onmouseover=/gi, '');
  
  return sanitized;
}

// 获取新的临时邮箱地址
async function generateNewEmail() {
  try {
    const response = await fetch('/api/email/generate')
    const data = await response.json()
    return data.address
  } catch (error) {
    console.error('Error generating email:', error)
    throw error
  }
}

// 获取邮件列表
async function fetchEmails() {
  const MAX_RETRIES = 3;
  let retries = 0;
  const WORKER_URL = 'https://email-worker.2668812066.workers.dev';
  while (retries < MAX_RETRIES) {
    try {
      const url = `${WORKER_URL}/emails?address=${encodeURIComponent(emailAddress.value.trim())}`;
      console.log('发送请求:', url);
      
      // 创建一个 AbortController 用于超时取消请求
      const controller = new AbortController();
      const signal = controller.signal;
      
      // 设置5秒超时
      const timeoutId = setTimeout(() => {
        controller.abort();
        console.warn('请求超时，已取消');
      }, 5000);
      
      const response = await fetch(url, { signal });
      
      // 清除超时定时器
      clearTimeout(timeoutId);
      
      if (!response.ok) {
        const errorText = await response.text();
        console.error('API错误:', response.status, errorText);
        throw new Error(`获取邮件失败: ${response.status} ${errorText}`);
      }
      
      const data = await response.json();
      console.log('API返回数据:', data);
      
      if (data && Array.isArray(data)) {
        emails.value = data;
        
        if (data.length > 0 && !selectedEmail.value) {
          selectedEmail.value = data[0];
        }
      } else {
        console.error('返回数据格式不正确:', data);
        throw new Error('返回数据格式不正确');
      }
      
      return; // 成功获取
    } catch (error) {
      // 检查是否是超时错误
      if ((error as Error).name === 'AbortError') {
        console.error('请求超时');
        // 不再重试，直接进入下一轮检查
        break;
      }
      
      console.error('获取邮件失败:', error);
      retries++;
      
      if (retries >= MAX_RETRIES) {
        // 只在界面手动点击检查时显示错误通知，自动轮询时不显示
        if (!isChecking.value) {
          showNotification('获取邮件失败，请稍后重试', 'error');
        }
        break;
      }
      
      // 等待一段时间后重试
      await new Promise(resolve => setTimeout(resolve, 1000));
    }
  }
}

// 复制邮箱地址
async function copyEmail() {
  try {
    await navigator.clipboard.writeText(emailAddress.value)
    showNotification('邮箱地址已复制到剪贴板')
  } catch (err) {
    showNotification('复制失败，请手动复制', 'error')
  }
}

// 刷新邮箱地址
async function refreshEmail() {        
  isCreatingEmail.value = true
  try {
    const chars = 'abcdefghijklmnopqrstuvwxyz0123456789';
    let username = '';
    for (let i = 0; i < 8; i++) {
      username += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    const address = `${username}@liaoxiang.fun`;
    emailAddress.value = address
    
    // 保存新生成的邮件地址到 localStorage
    localStorage.setItem('tempEmailAddress', address)
    
    emails.value = []
    selectedEmail.value = null
    showNotification('已生成新的临时邮箱地址')
  } catch (error: any) {
    showNotification('生成新邮箱失败', 'error')
  } finally {
    isCreatingEmail.value = false
  }
}

// 检查新邮件
async function checkNewMails() {
  if (!emailAddress.value) {
    showNotification('请先创建邮箱地址', 'error')
    stopAutoCheck()
    return
  }
  
  try {
    await fetchEmails()
  } catch (err) {
    console.error('邮件检查错误:', err);
    
    // 仅在手动检查邮件时显示错误提示，自动轮询时不显示
    if (!isChecking.value) {
      showNotification('检查邮件失败', 'error')
    }
  }
}

// 开始自动检查邮件
function startAutoCheck() {
  isChecking.value = true
  
  // 立即执行一次检查
  checkNewMails()
  
  // 清除可能存在的旧定时器
  if (autoCheckTimer.value !== null) {
    clearInterval(autoCheckTimer.value)
  }
  
  // 清除可能存在的旧的结束定时器
  if (autoCheckEndTimer.value !== null) {
    clearTimeout(autoCheckEndTimer.value)
    autoCheckEndTimer.value = null
  }
  
  // 设置每5秒自动检查一次
  autoCheckTimer.value = window.setInterval(() => {
    checkNewMails()
  }, 5000)
  
  // 设置1分钟后自动停止检查
  autoCheckEndTimer.value = window.setTimeout(() => {
    stopAutoCheck()
    showNotification('自动检查已完成')
  }, 60000) // 60秒 = 1分钟
}

// 停止自动检查
function stopAutoCheck() {
  // 清除检查邮件定时器
  if (autoCheckTimer.value !== null) {
    clearInterval(autoCheckTimer.value)
    autoCheckTimer.value = null
  }
  
  // 清除结束定时器
  if (autoCheckEndTimer.value !== null) {
    clearTimeout(autoCheckEndTimer.value)
    autoCheckEndTimer.value = null
  }
  
  isChecking.value = false
}

// 在组件卸载时清除定时器
onUnmounted(() => {
  stopAutoCheck()
})

// 选择邮件
function selectEmail(email: Email) {
  selectedEmail.value = email
}

// 显示通知
function showNotification(message: string, type: 'success' | 'error' = 'success') {
  notification.message = message
  notification.type = type
  notification.show = true
  setTimeout(() => {
    notification.show = false
  }, 3000)
}

// 清空收件箱
function clearEmails() {
  if (emails.value.length === 0) return;
  
  emails.value = [];
  selectedEmail.value = null;
  showNotification('收件箱已清空');
}
</script>